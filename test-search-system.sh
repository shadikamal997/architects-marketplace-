  body=$(echo "$response" | head -n -1)
  
  # Check HTTP status
  if [ "$status_code" != "$expected_status" ]; then
    echo "âŒ FAIL: Expected status $expected_status, got $status_code"
    echo "Response: $body" | head -n 5
    FAIL_COUNT=$((FAIL_COUNT + 1))
    echo ""
